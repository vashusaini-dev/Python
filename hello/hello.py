def main():
    """
    The main function of the program.
    
    What it does:
    - Encapsulates the program logic in a function (good practice for modular code).
    - Prints a greeting message to the console.
    """
    # print() is a built-in Python function that outputs text to the console
    print("Hello, World!")  # The classic first program output
    print("This is Vashu Saini")


# This line ensures that main() runs only when the script is executed directly
# and not when imported as a module in another script.
if __name__ == "__main__":
    main()  # Call the main function
